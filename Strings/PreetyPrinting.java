public class PreetyPrinting {
    public static void main(String args[]) {
        float n = 123.4567f;
        System.out.printf("Formatted value is: %.2f \n", n);
        System.out.printf("Pie: %.3f \n", Math.PI);
        System.out.printf("Hello my name is %s and I'm hot as %s.", "Pallavi", "Sun");
    }
}
